package loops;

public class DoWhile {
    public static void main(String[]args){

        //we gonna use do-while loop/
        //do while loop means 1st do then check.

        int counter=1;
        do{
            System.out.println("three types of loop in java->for,while,do while"+ " "+counter) ;
            counter++;
        }
        while(counter<=10);
    }
}
